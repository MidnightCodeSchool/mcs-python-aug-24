# Type
## Integer (เลขจำนวนเต็ม)
print("Typeof 42, 0, -42:")
print(type(42))
print(type(0))
print(type(-42))
## Float (เลขทศนิยม)
print("Typeof 42.1, 0.0, -42.42:")
print(type(42.1))
print(type(0.0))
print(type(-42.42))
## type ของผลลัพธ์จากการบวก, ลบ, คูณ, หาร เปลี่ยนได้
print("Typeof 2 / 3:")
print(type(2 / 3))
print("Typeof 2 + 3.1:")
print(type(2 + 3.1))
print("Typeof 2 - 3.1:")
## String
print("Typeof 'Hello':")
print(type('Hello'))
print("Typeof '9':")
print('9')
