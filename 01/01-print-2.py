# Operator
## Addition
print("Addition: 2 + 3:")
print(2 + 3)
print("Addition: 2 + 3 + 4:")
print(2 + 3 + 4)
## Subtraction
print("Subtraction: 2 - 3:")
print(2 - 3)
print("Subtraction: 2 - 3 - 4:")
print(2 - 3 - 4)
## Multiplication
print("Multiplication: 2 * 3:")
print(2 * 3)
print("Multiplication: 2 * 3 * 4:")
print(2 * 3 * 4)
## Division
print("Division: 2 / 3:")
print(2 / 3)
## Exponentiation
print("Exponentiation: 2 ** 3:")
print(2 ** 3) # 2*2*2
# this is a comment print(2 ** 3)
## Modulo (เศษจากการหาร)
print("Modulo: 10 % 3:")
print(10 % 3)
print("Modulo: 100 % 30:")
print(100 % 30)
## Floor Division (หารเอาส่วน)
print("Floor Division: 10 // 3:")
print(10 // 3)

