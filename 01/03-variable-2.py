# Reassigning a variable
a = 5
a = 6
print(a)

b = 6
b = a + 1
print(b)

### Incrementing a variable
print("""
c = 5
c = c + 1
""")
c = 5
c = c + 1
print(c)
print("""
d = 5
d += 1  # This is the same as d = d + 1
""")
d = 5
d += 1  # This is the same as d = d + 1
print(d)
### Decrementing a variable
print("""
e = 5
e -= 1  # This is the same as e = e - 1
""")
e = 5
e -= 1  # This is the same as e = e - 1
print(e)